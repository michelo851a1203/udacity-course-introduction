import csv
import re

with open('texts.csv', 'r') as f:
    reader = csv.reader(f)
    texts = list(reader)

with open('calls.csv', 'r') as f:
    reader = csv.reader(f)
    calls = list(reader)

teleMarketerCount = 0
for callItem in calls:
    if re.match(r"^140((?!\s).)*?$", callItem[0]) != None:
        teleMarketerCount += 1

print("These numbers could be telemarketers: {count}".format(
    count=teleMarketerCount,
    ))

